#include <stdio.h>

/**
 * main - Prints the program name, followed by a new line
 * @argc: the number of arguments in the program
 * @argv: array of pointers to the argument
 * Return: Default 0
 */

int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
